def main() -> None:
    text = OVERLAY.read_text(encoding="utf-8")
    # backup once
    if not BACKUP.exists():
        BACKUP.write_text(text, encoding="utf-8")
        print(f"backup → {BACKUP.name}")

    n_total = 0
    for han, kor in MAPPING:
        before = text.count(han)
        if before == 0:
            continue
        text = text.replace(han, kor)
        n_total += before
        print(f"  {han:>8} → {kor:<18}  ×{before}")
    OVERLAY.write_text(text, encoding="utf-8")

    # Final sanity: validate JSON
    try:
        json.loads(text)
        print(f"\n✓ JSON still valid. total replacements: {n_total}")
    except json.JSONDecodeError as e:
        print(f"\n❌ JSON BROKEN: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
